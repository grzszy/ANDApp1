package com.example.myapplication.Model;

public class Score {

    private int bestScore;
    private int score;

    public Score()
    {
        this.bestScore = bestScore;
        this.score = score;
    }

    public int getBestScore()
    {
        return bestScore;
    }

    public void setBestScore(int bestScore)
    {
        this.bestScore = bestScore;
    }

    public int getScore()
    {
        return score;
    }

    public void setScore(int score)
    {
        this.score = score;
    }
}
